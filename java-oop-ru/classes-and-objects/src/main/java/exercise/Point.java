package exercise;

// BEGIN
public class Point {
    private Integer x;
    private Integer y;

    public Point(Integer x, Integer y) {
        this.y = y;
        this.x = x;
    }

    public Point() {
    }

    public Integer getY() {
        return y;
    }

    public Integer getX() {
        return x;
    }
}
// END
